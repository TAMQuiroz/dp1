package Model;

/**
 *
 * @author erickelme
 */
public class Ubigeo {
    private long id;
    private String code;
    private String name;
    private String description;
    private String status;
    private ElectoralProcess electoralProcess;
    
    
    public void Ubigeo(){
        
    }
    /**
     * @return the id
     */
    public long getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(long id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description the description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * @return the electoralProcess
     */
    public ElectoralProcess getElectoralProcess() {
        return electoralProcess;
    }

    /**
     * @param electoralProcess the electoralProcess to set
     */
    public void setElectoralProcess(ElectoralProcess electoralProcess) {
        this.electoralProcess = electoralProcess;
    }

    /**
     * @return the status
     */
    public String getStatus() {
        return status;
    }

    /**
     * @param status the status to set
     */
    public void setStatus(String status) {
        this.status = status;
    }

    /**
     * @return the code
     */
    public String getCode() {
        return code;
    }

    /**
     * @param code the code to set
     */
    public void setCode(String code) {
        this.code = code;
    }
}
